// ==================== Handler ====================

export default function fileMock(config: InternalAxiosRequestConfig): any | null {
  const url = config.url ?? ''
  const method = (config.method || 'get').toLowerCase()

  // --- POST /files/upload/single (most specific, check first) ---
  if (method === 'post' && match('/files/upload/single', url)) {
    return ok({
      fileId: mockId('file'),
      fileName: 'mock-upload.pdf',
      fileUrl: '/mock/files/mock-upload.pdf',
      fileSize: 1024000,
      mimeType: 'application/pdf',
      category: 'document',
    })
  }

  // --- POST /files/upload ---
  if (method === 'post' && match('/files/upload', url)) {
    return ok([
      {
        fileId: mockId('file'),
        fileName: 'mock-upload-1.pdf',
        fileUrl: '/mock/files/mock-upload-1.pdf',
        fileSize: 1024000,
        mimeType: 'application/pdf',
        category: 'document',
      },
    ])
  }

  // --- DELETE /files/:id ---
  if (method === 'delete' && match('/files/:id', url)) {
    return ok('deleted')
  }

  // --- GET /files/my ---
  if (method === 'get' && match('/files/my', url)) {
    return ok(MOCK_FILES)
  }

  return null
}
